raj = "Anisha Das"

# string .count()
count = raj.count("a")
print(count)

print(raj.count("s"))

# string .find()
index = raj.find("Das")             # it returns the index value of first letter of that word
print(index)

# string .replace()
replaced_string = raj.replace("Das" , "Raj")
print(replaced_string)